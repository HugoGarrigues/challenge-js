const argument1 = 13;
const argument2 = 11;

function more(argument1){
    console.log(argument1+1);
}

function less(argument1){
    console.log(argument1-1);
}

function add(argument1,argument2){
    console.log(argument1+argument2);
}

function substract(argument1,argument2){
    console.log(argument1-argument2);
}

more(argument1);
less(argument1);
add(argument1,argument2);
substract(argument1,argument2);
